import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const scriptPath = fileURLToPath(import.meta.url);
const scriptsDirectory = path.dirname(scriptPath);
const vscodeRoot = path.resolve(scriptsDirectory, '..');
const repoRoot = path.resolve(vscodeRoot, '..', '..');

const requirementsPath = path.join(repoRoot, 'docs', 'requirements.md');
const ssdGrammarPath = path.join(repoRoot, 'docs', 'ssd.ebnf');
const ssqGrammarPath = path.join(repoRoot, 'docs', 'ssq.ebnf');
const outputPath = path.join(vscodeRoot, 'server', 'src', 'generated', 'hoverContract.ts');

const requirementsText = fs.readFileSync(requirementsPath, 'utf8');
const ssdGrammarText = fs.readFileSync(ssdGrammarPath, 'utf8');
const ssqGrammarText = fs.readFileSync(ssqGrammarPath, 'utf8');

const displayNames = {
  ssd: extractDisplayName(requirementsText, '.ssd'),
  ssm: extractDisplayName(requirementsText, '.ssm'),
  ssq: extractDisplayName(requirementsText, '.ssq')
};

const projection = {
  topLevel: {
    document: grammarEntry(ssdGrammarText, 'document-block', 'docs/ssd.ebnf'),
    resource: grammarEntry(ssdGrammarText, 'resource-block', 'docs/ssd.ebnf'),
    segment: grammarEntry(ssdGrammarText, 'segment-block', 'docs/ssd.ebnf'),
    assertion: grammarEntry(ssdGrammarText, 'assertion-block', 'docs/ssd.ebnf'),
    hypothesis: grammarEntry(ssdGrammarText, 'hypothesis-block', 'docs/ssd.ebnf'),
    alternative: grammarEntry(ssdGrammarText, 'alternative-block', 'docs/ssd.ebnf'),
    document_meta: grammarEntry(ssdGrammarText, 'document-meta-block', 'docs/ssd.ebnf'),
    meta: grammarEntry(ssdGrammarText, 'meta-block', 'docs/ssd.ebnf')
  },
  nested: {
    meta: grammarEntry(ssdGrammarText, 'inline-meta-block', 'docs/ssd.ebnf'),
    embedding: grammarEntry(ssdGrammarText, 'embedding-block', 'docs/ssd.ebnf')
  },
  queryHeader: {
    query: grammarEntry(ssqGrammarText, 'query-block', 'docs/ssq.ebnf')
  },
  queryEntry: {
    select: grammarEntry(ssqGrammarText, 'select-entry', 'docs/ssq.ebnf'),
    where: grammarEntry(ssqGrammarText, 'where-entry', 'docs/ssq.ebnf'),
    similar: grammarEntry(ssqGrammarText, 'similar-entry', 'docs/ssq.ebnf'),
    return: grammarEntry(ssqGrammarText, 'return-entry', 'docs/ssq.ebnf')
  }
};

fs.mkdirSync(path.dirname(outputPath), { recursive: true });
fs.writeFileSync(outputPath, renderModule(displayNames, projection));

function extractDisplayName(text, extension) {
  const escapedExtension = extension.replace('.', '\\.')
  const pattern = new RegExp(`^- .*${escapedExtension}.*正式表示名は (.+?) とする$`, 'm');
  const match = text.match(pattern);
  if (!match) {
    throw new Error(`Could not find display name for ${extension} in requirements.md`);
  }
  return match[1];
}

function grammarEntry(grammarText, productionName, sourcePath) {
  const excerpt = extractProduction(grammarText, productionName).replace(/\r/g, '').replace(/\n\s+/g, ' ');
  return {
    productionName,
    excerpt,
    sourcePath
  };
}

function extractProduction(grammarText, productionName) {
  const escapedProductionName = productionName.replace(/[-/\\^$*+?.()|[\]{}]/g, '\\$&');
  const pattern = new RegExp(`^\\s*${escapedProductionName}\\s*=([\\s\\S]*?);`, 'm');
  const match = grammarText.match(pattern);
  if (!match) {
    throw new Error(`Could not find production ${productionName}`);
  }
  return `${productionName} =${match[1]};`;
}

function renderModule(displayNamesValue, projectionValue) {
  return [
    '// This file is generated by scripts/generate-hover-contract.mjs.',
    '// It is a runtime projection of docs/requirements.md, docs/ssd.ebnf, and docs/ssq.ebnf.',
    '',
    `export const semdlDisplayNames = ${serialize(displayNamesValue)} as const;`,
    '',
    `export const keywordHoverProjection = ${serialize(projectionValue)} as const;`,
    ''
  ].join('\n');
}

function serialize(value) {
  return JSON.stringify(value, null, 2);
}